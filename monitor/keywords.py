# Платформы, где используем широкие триггеры
BROAD_PLATFORMS = {"chinapostman", "telegram"}


def match_keywords(text: str, platform: str = "") -> str | None:
    """Проверяет текст на совпадение с ключевыми словами.
    Возвращает первое совпавшее ключевое слово или None.

    platform — если передана и входит в BROAD_PLATFORMS,
    дополнительно проверяются широкие триггеры.
    """
    text_lower = text.lower()
    for kw in KEYWORDS_EXACT:
        if kw.lower() in text_lower:
            return kw
    if platform in BROAD_PLATFORMS:
        for kw in KEYWORDS_BROAD:
            if kw.lower() in text_lower:
                return kw
    return None
